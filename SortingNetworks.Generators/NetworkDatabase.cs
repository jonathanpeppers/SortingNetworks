        /// Returns the comparator pairs for the given network size as a flat array [i0, j0, i1, j1, ...].
        /// Each layer is separated by the layer sizes returned from <see cref="GetLayerSizes"/>.
        /// Returns null if the size is not in the database (use Batcher's algorithm instead).
        /// </summary>
        internal static int[]? GetNetwork(int size)
        {
            if (size < 2 || size >= Networks.Length || Networks[size] == null)
                return null;
            return Networks[size];
        }

        /// <summary>
        /// Returns the layer sizes for the given network size.
        /// Each layer contains disjoint comparator pairs that can execute in parallel (SIMD).
        /// Returns null if the size is not in the database.
        /// </summary>
        internal static int[]? GetLayerSizes(int size)
        {
            if (size < 2 || size >= LayerSizes.Length || LayerSizes[size] == null)
                return null;
            return LayerSizes[size];
        }

